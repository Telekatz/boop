#define _FLASH_BASE_ADDR (0x82000000)


int main(void)
{
	
	//*************************************************************************************
	
	//*************************************************************************************


  int r;

  Init(0, 0, 0);

  // Erase sector
  r = EraseSector(_FLASH_BASE_ADDR);
  if (r != 0) {  // Error?
    while (1);
  }

  // Blank Check
  r = BlankCheck(_FLASH_BASE_ADDR, sizeof(_acTestData), 0xff);
  if (r != 0) {  // Error?
    while (1);
  }

  // Program page
  r = SEGGER_OPEN_Program(_FLASH_BASE_ADDR, sizeof(_acTestData), (U8 *)_acTestData);
  if (r != 0) {  // Error?
    while (1);
  }

  // Verify
  r = Verify(_FLASH_BASE_ADDR, sizeof(_acTestData), (U8 *)_acTestData);
  if (r != _FLASH_BASE_ADDR + sizeof(_acTestData)) {  // Error?
    while (1);
  }


  UnInit(0);

  IODIR0 |= (1<<4);

	unsigned long x;
	
	while (1)
	{	

	  x=1000000;
	  while(x--) {};
	  IOSET0 |= (1<<4);   // sck0/P0.4
	  x=1000000;
	  while(x--) {};
	  IOCLR0 |= (1<<4);    // sck0/P0.4

	
	}
	return 0;
}

